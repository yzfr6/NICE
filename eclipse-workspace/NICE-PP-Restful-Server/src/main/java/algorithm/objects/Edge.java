package algorithm.objects;

public class Edge {

	public int id;
	public int source_id;
	public int target_id;
	public int type;
	public Edge(int db_id, int source_db_id, int target_db_id, int type) {
		super();
		this.id = db_id;
		this.source_id = source_db_id;
		this.target_id = target_db_id;
		this.type = type;
	};
	
	
	
	
	
}
